public class Bai10 {
    public static void main(String[] args) {
        int a=5;
        int b=6;

        System.out.printf(" a" +a);
        System.out.printf(" b" +b);
        //eps kiểu ngầm định
        float c= (float) a;
        float d= (float) b;
        System.out.printf(" c" +c);
        System.out.printf(" d" +d);
        float e=3.5f;
        float f=4.5f;
        System.out.printf(" e=" +e);
        System.out.printf(" f=" +f);
        int h= (int)e;
        int g= (int)f;
        System.out.printf(" h=" +h);
        System.out.printf(" g=" +g);
    }
}
